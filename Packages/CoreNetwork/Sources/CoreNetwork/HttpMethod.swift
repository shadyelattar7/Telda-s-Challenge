public enum HttpMethod: String {
    case get     = "GET"
    case post    = "POST"
    case patch   = "PATCH"
    case delete  = "DELETE"
    case connect = "CONNECT"
    case head    = "HEAD"
    case options = "OPTIONS"
    case put     = "PUT"
    case trace   = "TRACE"
}
